	public static final EntityDataAccessor<Integer> DATA_Target_X = SynchedEntityData.defineId(BeamEntityEntity.class, EntityDataSerializers.INT);
	public static final EntityDataAccessor<Integer> DATA_Target_Y = SynchedEntityData.defineId(BeamEntityEntity.class, EntityDataSerializers.INT);
	public static final EntityDataAccessor<Integer> DATA_Target_Z = SynchedEntityData.defineId(BeamEntityEntity.class, EntityDataSerializers.INT);

